=head3 cronjob_nightly

Plugin hook running code from a cron job

=cut

sub cronjob_nightly {
    my ( $self ) = @_;

    my $dbh = C4::Context->dbh;
    $dbh->do(q{
        UPDATE items
        SET    price = Round(price, 1)
        WHERE  ( price NOT LIKE '%0'
                 AND price NOT LIKE '%5' )  
    });

    $dbh->do(q{
        UPDATE items
        SET    replacementprice = price  
    });
}

1;
